function mapFromSymbols(rows) {
  return rows.map((row, rowIndex) =>
    [...row].map(symbol => {
      if (!(symbol in TILE_SYMBOLS)) {
        throw new Error(`Unknown tile symbol: ${symbol} at row ${rowIndex}`);
      }
      return TILE_SYMBOLS[symbol];
    })
  );
}


function createHouseInteriorMap() {
  const cols = 32;
  const rows = 24;
  const doorStart = Math.floor(cols / 2) - 1;
  const doorEnd = doorStart + 2;

  return mapFromSymbols(Array.from({ length: rows }, (_, y) => (
    Array.from({ length: cols }, (_, x) => {
      const isDoor = y >= rows - 2 && x >= doorStart && x <= doorEnd;
      const isWall = x < 2 || x >= cols - 2 || y < 2 || y >= rows - 2;
      return isWall && !isDoor ? 'T' : 'S';
    }).join('')
  )));
}

